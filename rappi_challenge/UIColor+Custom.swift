    }
    
    static var customBlue: UIColor {
        return UIColor(red: 31/250, green: 168/250, blue: 207/250, alpha: 1)
    }
    
    static var customRed: UIColor {
        return UIColor(red: 245/250, green: 113/250, blue: 112/250, alpha: 1)
    }
    
}
